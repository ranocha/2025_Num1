# ╔═╡ f51e9845-7aa1-4afa-8b19-4e18eb8a1ad8
function demonstrate_bisection(f, a::T, b::T) where {T}
	steps = 1:100
	as = Vector{T}(undef, length(steps))
	bs = Vector{T}(undef, length(steps))
	fs = Vector{T}(undef, length(steps))

	for i in steps
		as[i] = a
		bs[i] = b
		c = (a + b) / 2
		fc = f(c)
		fs[i] = fc

		if iszero(fc)
			a, b = c, c
		elseif fc < 0
			a = c
		else # fc > 0
			b = c
		end
	end

	data = hcat(fs, as, bs, bs - as, (bs .- as) ./ bs)
	header = ["f(cₙ)", "aₙ", "bₙ", "bₙ - aₙ", "(bₙ - aₙ) / bₙ"]
	formatters = (ft_printf("%1.12f", [2, 3]),
				  ft_printf("%1.3e", [1, 4, 5]))
	
	pretty_table(HTML, data; 
				 header, alignment = :l, formatters,
				 row_labels = axes(data, 1))
end

